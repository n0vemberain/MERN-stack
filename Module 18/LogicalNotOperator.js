// Logical Not Operator
let isRaining = false;
if (!isRaining) {
    console.log("It's not raining, you can go outside without an umbrella.");
} else {
    console.log("It's raining, don't forget your umbrella!");
}

let isLoggedIn = true;
if (!isLoggedIn) {
    console.log("Please log in to continue.");
} else {
    console.log("Welcome back!");
}